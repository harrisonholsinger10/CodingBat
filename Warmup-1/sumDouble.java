package com.example.demo;

public class sumDouble
{
    public int sumDouble(int a, int b) {
        if (a != b)
        {
            int aPlusB = a + b;
            return aPlusB;
        }
        else
        {
            int doubleAB = 2 * (a + b);
            return doubleAB;
        }
    }


}
